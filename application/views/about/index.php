    嗨！各位新生你們好！求
學的新階段總是令人興奮，特
別是從高中解脫的你們，對於
新生活總是既期待又怕受傷害
。身為學長姐的我們，曾經也
走過這段，心情也正是如此，
像個無頭蒼蠅毫無目的，正是
聽到你們的呼叫，化身為「大
一生活知訊網」，成為你們的
＂校園網路導覽員＂！


　　一年又一年的經驗傳承及追求創新，知訊網的進步、所有的付出，都
是為了讓新鮮人，帶著期待、愉快的心情來迎接大學生活，並能夠對校園
周遭的環境及資源有初步的認識，甚至是提早認識新朋友！有關宿舍、生
活、註冊....所有的一切，都可以透過知訊網來得知。

　　除了我們所提供的，當然還有許多神秘未知的問題，我們另外設置了
屬於各系及社團的論壇專區，讓大家能跟系上同學、學長姐彼此交流，不
錯過任何即時訊息，更深入地瞭解中大的一切！

　　非常感謝新鮮人們使用「２０１２大一生活知訊網」，在十月我們將
完成團隊的使命－＂帶領各位熟悉中大校園＂。在十月之後，我們團隊將
會傳承給下一代的你們，讓知訊網能夠薪火相傳。如果你有感受到知訊網
的用心，願意為下一屆的學弟妹們盡一份心力，並且對知訊網有很多想法
及熱情，歡迎你們加入，「２０１２大一生活知訊網」需要你唷！詳細在
開學後的十月、十一月，將會在校園各處或是ＢＢＳ上張貼訊息、公布招
募資訊，屆時將有更完整的資訊，請大家敬請期待！</PRE>
