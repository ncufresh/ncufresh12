<p>
    (三)健康資料表置於<strong>本資料袋內</strong>，請自行填寫完畢並貼妥最近光面照片二吋乙張，檢查當日請攜帶本資料表受檢。如無法到校健檢者，請攜帶健康資料表至地區醫院以上之醫療院所完成健檢，於<strong>9月13日前</strong>將表格交至衛保組。
