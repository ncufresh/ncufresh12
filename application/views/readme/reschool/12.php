<h4>
    復學生大一英文修課規定
</h4>
<ol>
    <li>
        需重/補修「大一英文」，但未參加過本校「大一英文分級測驗」者，請於9/10~9/12每日上午11:00至綜教館一樓O-104教室參加分級測驗（擇一參加，不需事先報名）。未事先經過分級者，不得選修本校大一英文課程。相關問題請洽語言中心(分機：33816)。
    </li>
    <li>
        近四年內曾在國內外其他大學院校修習性質、學分數與本校大一英文相同之課程且成績及格者，可辦理大一英文課程抵免。欲申請抵免者請填寫「國立中央大學學生學分抵免申請表」，並檢附<u>原校成績單</u>，依本校規定之時程至語言中心辦理。
    </li>
    <li>
        休學前已完成辦理者可以免辦理。
    </li>
</ol>