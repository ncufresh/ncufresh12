<h4>
    大一國文寫作檢定
</h4>
<p>
    大一新生及復學生(含僑生中文能力分級之高級組學生)皆須參加中文寫作檢定，本次檢定亦列為大一國文成績評量之一。
    <strong><u>未參加者，一律須參加由本中心規劃的補救學習。</u></strong>
</p>
<p>
    (一)本校自93學年度起，除免修大一國文者外(抵免規定請洽詢大一國文教學研究中心，分機：33100)，新生(含復學生)皆須參加中文寫作檢定。考試時間50分鐘。
</p>
<p>
    (二)加退選時間(<strong>9</strong><strong>月12日～9月25日，9月25日只可退選</strong>)結束後，於大一國文課程上進行寫作檢定，請學生至所屬的大一國文課堂上參加測驗。當天未到課者，一律需參加為期6週的補救學習。檢定時間暫訂為下列時段，若有任何異動，以任課老師宣布的時間為準。
</p>
