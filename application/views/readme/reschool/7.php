<p>
(二)如已申請學校公務SPARC(E-mail帳號)，請直接進入第四項之登錄方式。如尚未申請，請至<strong><a href="http://www.cc.ncu.edu.tw/sparc"><u>http://www.cc.ncu.edu.tw/sparc</u></a>之</strong>「新生專區」<strong>啟動您的E-mail帳號(學號)</strong>，及<strong>設定您的密碼</strong>。這組帳號密碼敬請牢記，它提供您在學校的電子郵件信箱、個人網頁空間Portal及電算中心的其他服務，例如學生宿舍網路申請、學期選課、查詢學期成績、使用中心的電腦、印表機&hellip;等。完成後請稍待5分鐘(系統處理時間)，再以<strong>自設密碼</strong>進行登錄。</p>
