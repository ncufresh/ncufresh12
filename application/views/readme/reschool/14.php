<h4>
    申請就學貸款須知
</h4>
<p>
    一、申請資格：(符合下列條件之一項者皆可辦理)
</p>
<p>
    （一）家庭年收入114 萬元以下者(以稅務單位計算的所得為準)，指學生本人及家長(含監護人)，已婚者則為本人及配偶，貸款利息均由政府負擔。
</p>
<p>
    （二）同上計列，家庭年收入在114~120萬元之間者，貸款人與政府各付一半利息。
</p>
<p>
    （三）同上計列，家庭年收入在120萬元以上，且學生本人及其兄弟姐妹有二人以上就讀高中職以上學校者，但利息全數自行負擔。《請備妥另一兄弟姐妹之學生證影本，並需蓋有學期註冊章》
</p>
<p>
    備註：(A)家庭年收入查核「年度」：為申請學年度之前一年度，例100學年上下學期申請，是查民國99年之年所得。
</p>
<p>
    (B)先申請後查核：開學後，本校將申貸者資料送教育部，由教育部送財稅中心查核家庭收入，資格不符者將取消申貸，並請自行補繳學費。
</p>
<p>
    二、下列學生不能申請貸款：
</p>
<p>
    已享受全部公費待遇者、全部學雜費減免或已獲得政府主辦之其他無息助學貸款者，但享受部分公費、部分減免或已請領教育補助費者，其差額部份仍可申請貸款。
</p>
<p>
    三、貸款程序：
</p>
<p>
    （一）<strong>「研究生」及「大學部延畢生」必需先上網預估學分數</strong>
</p>
<p>
    1.<strong>中大首頁&rarr;<a href="http://portal.ncu.edu.tw/" title="Portal">Portal</a>&rarr;就學補助系統&rarr;&hellip;。</strong>
</p>
<p>
    2.<strong>中大首頁</strong><strong>&rarr;</strong><strong>在校生</strong><strong>&rarr;</strong><strong>助學資訊－就學補助系統</strong>
    <strong>&rarr;(</strong><strong>進入)輸入ID及密碼</strong><strong>&rarr;</strong>
    <strong>就學補助系統</strong><strong>&rarr;</strong><strong>就學貸款</strong>
    <strong>&rarr;</strong><strong>&hellip;</strong><strong>。</strong>
    <strong>於登錄學分後，繳費單上始會顯示學分費，據此向臺灣銀行貸款，選課確定後再多退少補。</strong>
</p>
<p>
    3.<strong>或<a href="http://web.cc.ncu.edu.tw/~ncu7221/" title="生輔組網站">生輔組網站</a>&rarr;就學補助系統登錄&rarr;&hellip;。</strong>
</p>
<p>
    （二）收到學校的繳費單或自行上網下載列印後，需至<strong><a href="https://sloan.bot.com.tw/sloan/sLoanLogin.do" title="臺灣銀行就學貸款入口網站"><u>臺灣銀行就學貸款入口網站</u></a>  執行就貸線上申請</strong>填寫基本資料及預約辦理時間。
</p>
<p>
    （三）備齊下列文件，親至臺灣銀行各地分行辦理借貸手續：
</p>
<p>
    1、攜帶學生證(新生憑錄取通知單)、繳費通知單、國民身分證、印章、本人及保證人最近三個月戶籍謄本(第一次申貸者)，學生及家長(保證人)本人親自至銀行辦理對保手續。
</p>
<p>
    2、每一教育階段(高中、專科、大學、研究所各自為一階段，並無連續)第一次辦理時需由家長或監護人陪同，爾後由學生自己攜帶身分證、印章、戶籍謄本至銀行辦理借款手續即可。
</p>
<p>
    3、學生未滿二十歲者：
</p>
<p>
    （1）應由父親及母親共同擔任保證人。
</p>
<p>
    （2）非雙親家庭者，由監護人擔任保證人。
</p>
<p>
    四、至本校<strong>教務處「學籍及成績系統」</strong>填寫「郵局局帳號」、「聯絡電話」、「戶籍與聯絡地址」等<strong>個人基本資料</strong>(<a href="http://www4.is.ncu.edu.tw/register/student.php" title="登入學籍系統">登入學籍系統</a>)
</p>
<p>
    &agrave;點選學籍登錄<strong>&rarr;</strong>英文姓名確定無誤請打勾，然後點選資料確認無誤，送出&agrave;點選修改其他基本資料&agrave;確認郵局局帳號後，到畫面最下方點選 以下資料確認無誤（submit）即可)
</p>
<p>
    <strong>(</strong><strong>個人資料請務必填寫正確及清楚，俾利在申核過程中即時聯繫及偵錯更正)</strong>
</p>
<p>
    五、多貸的書籍費、校外住宿費，將於學期末退至個人帳戶；學分費多貸者，則退至銀行以按實貸款。
</p>
<p>
    六、註冊時繳交以下文件至<strong>學務處生活輔導組</strong>：
</p>
<p>
    台銀撥款通知書<strong>第二聯</strong>，逾期未繳件至<strong>生活輔導組</strong>者，視同未辦理就學貸款，屆時將補繳學費。<strong><em>(</em></strong>
    <strong><em>為縮短辦理註冊時間及流程，凡註冊週前已提早辦畢就學貸款手續者，可先行將就貸撥款通知書第二聯以掛號信郵寄至本校學務處生輔組吳先生收)</em></strong>
    <strong><em>。</em></strong>
</p>
<p>
    七、本須知如有未盡事宜，均依教育部所發布之<strong>「高級中等以上學校學生就學貸款辦法」、「高級中等以上學校學生就學貸款作業要點」及台灣銀行與本校有關「就學貸款」相關公告事項辦理之</strong>。
</p>
<p>
    八、若有疑問，歡迎洽詢學務處生活輔導組<strong>吳正華先生</strong>，電話：(03)422-7151轉<strong>57221~4</strong><strong>。</strong>
