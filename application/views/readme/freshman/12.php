<h1>
    學籍登錄
</h1>
<p>
    (一)系統開放時間：<strong>8月16日起。</strong>
</p>
<p>
    (二)先請至
    <a href="http://www.cc.ncu.edu.tw/sparc" title="國立中央大學電子計算中心">國立中央大學電子計算中心</a>
    <strong>&rarr;新生專區，啟動您的E-MAIL帳號(學號)，及設定您的密碼</strong>。這組帳號密碼敬請牢記，它提供您在學校的電子郵件信箱、個人網頁空間Portal及電算中心的其他服務，例如學生宿舍網路申請、學期選課、查詢學期成績、使用中心的電腦、印表機等。完成後請稍待5分鐘(系統處理時間)，再以
    <strong>自設密碼</strong>進行登錄。
</p>
<p>
    (三)登錄網址：<strong><a href="http://www.ncu.edu.tw" title="本校首頁">本校首頁</a>&rarr;在校生&rarr;教學資訊&rarr;學籍登錄/成績查詢。</strong>
</p>
<p>
    (四)登錄方式：以<strong>學號</strong>(註明於此須知郵寄袋外，亦可自行於
    <a href="http://ncufresh.ncu.edu.tw" title="大一生活知訊網">大一生活知訊網</a>中查詢
    <strong>，自設密碼</strong>進行登錄
    <strong>。</strong>欄位前打＊者為必填欄位，請務必填寫，方可儲存。
</p>
<p>
    (五)<strong>注意事項：學籍登錄為註冊應辦事項，新生應完成登錄及他註冊程序方可領取學生證</strong>。
</p>
<p>
    (六)相關問題請洽詢註冊組(分機：57111～3)。
</p>