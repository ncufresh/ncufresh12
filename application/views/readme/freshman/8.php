<h1>
導師輔導資源系統</h1>
<p>
本校設有導師制度，導師以提供課業及生涯諮詢輔導為主。導師將可透過「導師輔導資源系統」瞭解及關心您學習的發展，內容包括學籍及未來的成績、畢業審核表及學期選課等資料，成為您生涯發展的最佳諮詢對象。</p>
<p>
本系統將先預設您同意開放以上資料供導師查詢，但您可以在登錄學籍後隨時上網修改。</p>
