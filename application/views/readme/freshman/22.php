<h4>
	大一新生國文修課規定
</h4>
<h5>
	一、大一國文修課辦法
</h5>
<p>
	（一）本校大一國文之修課由本校中國文學系大一國文教學研究中心（以下簡稱中心）負責規劃及執行。
</p>
<p>
	（二）大一一般生修習國文課程
</p>
<ol>
	<li>
		入學時須參加大一新生中文寫作檢定。中心依上述中文寫作檢定成績，區分「通過」及「不通過」兩等級。未參加檢定及檢定結果為「不通過」者，必須參加補救學習，由本中心另行安排寫作輔導。寫作成績將列入各所屬老師學期成績考量內。
	</li>
	<li>
		各系大一學生依指定時段選修所開設之課程，不得跨越其他時段選課，且兩學期不得重複選修同一位教師所開之課程。
	</li>
	<li>
		大一復學生視同大一一般生，須參與中文寫作檢定。
	</li>
</ol>
<p>
	（三）大一僑生修習國文課程
</p>
<ol>
	<li>
		須參加僑生中文能力分級測驗。未參加者，不得修習大一國文。
	</li>
	<li>
		測驗結果為初級組者（不含中文系僑生、中文系外籍生），在修習「僑生國文」之前，必須先選修本校語言中心所開一學年四學分之「僑生先修華語Ⅰ」（LN0403）、「僑生先修華語Ⅱ」（LN0404），且此選修學分不列入畢業學分總數，通過之後方得修習「僑生國文」課程。
	</li>
	<li>
		測驗結果為中級組者（不含中文系僑生、中文系外籍生），僅能修習中文系所開「僑生國文」課程，不得修習其他時段國文課程。
	</li>
	<li>
		測驗結果為高級組者（不含中文系僑生、中文系外籍生），應依所屬學系時段修習一般大一國文課程。
	</li>
	<li>
		中文系僑生經測驗結果為初級組者，除須修習中文系大一國文課程外，必須選修本校語言中心所開一學年四學分之「僑生先修華語Ⅰ」（LN0403）、「僑生先修華語Ⅱ」（LN0404），且此選修學分不列入畢業學分總數。測驗結果為中、高級者，應修習中文系大一國文課程。
	</li>
</ol>
<p>
	（四）大一外籍生修習國文課程
</p>
<ol>
	<li>
		中文系外籍生須參加中文能力分級測驗。測驗結果為初級組者，除修習中文系大一國文課程，必須選修本校語言中心所開一學年四學分之「僑生先修華語Ⅰ」（LN0403）、「僑生先修華語Ⅱ」（LN0404），且此選修學分不列入畢業學分總數。測驗結果為中、高級者，應修習中文系大一國文課程。
	</li>
	<li>
		中文系外籍生須參加中文寫作檢定。未參加與不通過者，須參加補救學習。
	</li>
	<li>
		非中文系外籍生不需修習中文系所開設之國文課程，但須修習本校語言中心所開設六學分之華語課程，並於畢業前辦理國文課程抵修手續。
	</li>
</ol>
<p>
	（五）大二以上重補修生修習國文課程
</p>
<ol>
	<li>
		大二以上重補修生（不含僑生與外籍生），可跨時段選修大一國文課程，但兩學期不可重複選同一位教師所開之課程。
	</li>
	<li>
		大二以上僑生與外籍生修課同大一僑生與外籍生修課規定。
	</li>
</ol>
<p>
	（六）本辦法經教務會議通過後實施，修正時亦同。
</p>
<h5>
	二、1011大一國文選課說明
</h5>
<p>
