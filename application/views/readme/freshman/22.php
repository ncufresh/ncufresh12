<p>
	（二）選課流程
</p>
<p>
	（1） 大一國文採興趣選課制，請查詢所屬時段內開設的課程，詳細閱讀課程大綱（至選課網站點選課程流水號即可顯示），再依自己的興趣排列選課順位。因修習國文課程的學生人數眾多，請務必將所屬時段的所有課程皆列入您的志願排序，並詳細記錄每一門課程的課程流水號、課程名稱、教師姓名，不要專執於某一門課程，也不要在中選後見異思遷，多次退選、改選，造成最後無法選上任何一門課程之情形。
</p>
<p>
	（2）除僑生外，其他同學一律參加電腦選課，不得領取密碼卡，流程如下：
</p>
<ol>
	<li>
		進入中央大學教務處課務組首頁，網址為http://course.adm.ncu.edu.tw:8080/Course/main/news/announce 。
	</li>
	<li>
		登入系統後，點選畫面上方的「課程加退選」裡「選課」的選項，點選「依授課對象」後依序選擇「文學院」&rarr;「中國文學系」&rarr;	「中國文學系[不分類]」search大一國文課程列表。
	</li>
	<li>
		加選符合時段的大一國文課程並同時檢查課程名稱與教師姓名是否相符，再進入「step5 志願序及選修別」依個人喜好調整志願序。
	</li>
</ol>
<p>
	（3）僑生修習大一國文注意事項：
</p>
<p>
	所有僑生經過「中文能力分級測驗」後即區分為初級、中級、高級三組。
</p>
<ol>
	<li>
		初級組：須先選修一學年四學分的僑生先修華語課程（LN0403、LN0404）。該課程結束，學期成績合格者，才可修習僑生班國文。
	</li>
	<li>
		中級組：須修習「僑生班」國文課程。請務必於9月17-26日至中文系辦(文二館313室)領取密碼卡加選。
	</li>
	<li>
		高級組：須按所屬系別，修習Ｂ、Ｃ、Ｄ時段大一國文。請於9月17-26日至中文系辦公室(文二館313室)領取密碼卡並登記選修班級。
	</li>
</ol>
<p>
	（三）注意事項
</p>
<ol>
	<li>
		部分教師開設一班以上大一國文課程，分屬不同時段，雖課程名稱完全相同，但課程流水號不同，選課時請特別留意。
	</li>
	<li>
		國文課程選課分發規則，依序是：大一&gt;大四&gt;大三&gt;大二。於線上選課後，隔日請再上網檢查是否已選上所選的課程。如系統通知選填作業有誤，請再仔細查看所填寫的資料。
	</li>
	<li>
		各系大一學生僅能選修指定時段內所開設之課程，不得跨越其他時段選課。重補修生則不在此限。
	</li>
	<li>
		每班人數上限為五十二人，如所選之班級人數超額，選課系統將以亂數篩選。
	</li>
	<li>
		除僑生班（中文能力分級測驗結果為中級組學生，以及修畢四學分華語課程之初級組學生）與中文系學生之外，其餘時段的同學上下學期不可重複選同一位教師所開的課程。
	</li>
</ol>