    (七)本校宿舍床位多屬上舖，床板皆為固定尺寸，若學生身材較壯大或有特殊住宿需求之學生，請主動與生輔組宿舍承辦人聯繫，將協助調整床位，調整後將無法與同班、系同學一同住宿。
</p>
