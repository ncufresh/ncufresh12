<h4>
    大一新生宿舍注意事項
</h4>
<p>
    為使學生有良好生活習慣與精神體魄，培養自治、自律、明辨是非之中大人，本校實施學生宿舍自主、自律、榮譽與健康管理，請貴家長與學生明瞭本校大一新生宿舍生活學習之重點要求：
</p>
<ol>
    <li>
        為養成學生正常作息習慣，上課有精神，學習有效果，新生宿舍於夜間12時至清晨6時熄寢室大燈，走廊、衛浴等公共空間則仍有照明。
    </li>
    <li>
        為推動永續環保綠色校園，住宿學生應配合「空調適溫26~28度」、「隨手關閉電腦、關燈」、「節約用水」等節約能源、環境保護及消防安全之相關措施。
    </li>
    <li>
        校園內及宿舍區全面禁菸，請協助督促學生。
    </li>
    <li>
        本校宿舍已全面裝置冷氣，基於使用者付費原則，寢室冷氣電費由住戶自行購買儲值卡使用，請愛惜公物與秉持節約能源原則使用。
    </li>
    <li>
        夜間11時至上午7時，非本棟住宿生不能逗留於宿舍內，住宿學生應發揮守望相助精神，協助宿舍安全之維護。</li>
    <li>
        宿舍內不得有酗酒、賭博、毆鬥、吸煙、嚼食檳榔、打麻將、使用違禁藥品等行為，或妨害宿舍安全之任何情事。</li>
    <li>
        請提防詐騙活動，如利用師長名義佯稱學生出車禍詐騙、訂購物品詐騙、金融卡詐騙、利用手機簡訊詐騙、利用電話或書信通知中獎詐騙、退費轉帳詐騙等。遇有疑慮，切記先查證，或撥打165反詐騙電話，並通知教官或校安專員協助處理。
    </li>
    <li>
        其餘宿舍相關規定，請詳閱「學生宿舍管理辦法」，可在學生事務處網頁法規表格處下載。
    </li>
    <li>
        如有相關宿舍問題，請撥打以下分機洽詢。中央大學總機(03)422-7151
    </li>
</ol>
<p>
