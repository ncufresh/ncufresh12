<h4>
    性別平等
</h4>
<p>
    【<strong>生活中有些事，因性別而不同，你發現了嗎？</strong>】
</p>
<p>
    當身形高壯、說話低沉且力氣大的女生漸漸被認為是中性時，為什麼語調尖細、個子瘦小或愛打扮的男生卻還是依然被叫作CC或娘砲？
</p>
<p>
    為什麼當我們遇到亂開車的人時，常常會聯想到對方是位女駕駛？
</p>
<p>
    為什麼男廁的指標總是藍色，而女廁的指標都是紅色？
</p>
<p>
    為什麼男生總被鼓勵主動去追求女生，而女生若主動追求對方則被稱為「倒追」或「倒貼」？
</p>
<p>
    【<strong>懷孕不再需要獨自承受&hellip;</strong>】
</p>
<p>
    你知道嗎，在學期間懷孕了，學校是能夠幫忙的唷J例如~提供適當的無障礙學習環境、產假期間不列入修業年限、提供補助教學以及協助尋找校內外相關資源等！
</p>
<p>
    【<strong>性騷擾，你有權力大聲喊「不」！</strong>】
</p>
<p>
    你知道嗎，當對方用有性或性別歧視及騷擾的語言、肢體動作、圖片或文字等各種方式，讓你覺得不舒服時，就是性騷擾！你有權力大聲拒絕並尋求幫忙，保護自己，不讓性騷擾的行為延續！
</p>
<p>
    <strong>其實，性別平等，可以從你我開始做起！</strong>
</p>
