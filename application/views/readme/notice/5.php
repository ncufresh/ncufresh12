<p>
<strong>『導師輔導資源系統』</strong></p>
<p>
導師如何瞭解您需要的學習諮詢方向？</p>
<p>
導師可以透過已整合選課、畢業審核表及歷年成績等個人資料的<strong>『導師輔導資源系統』，</strong></p>
<p>
瞭解您需要的學習諮詢方向。</p>
<p>
在您完成學籍資料登錄後，你可以sparc帳號及密碼登入『導師輔導資源系統』；點選同意或不同意授權個人資料在本系統開放讓導師瞭解。系統將預設您為同意授權，但您可以隨時上網修改(『<a href="http://www4.is.ncu.edu.tw/osa_teacher/">導師輔導資源系統</a>』或由<a href="https://portal.ncu.edu.tw/wps/portal/!ut/p/cxml/04_Sj9SPykssy0xPLMnMz0vM0Q_wyU_PzNMvSFdUBACQIYPZ">中大portal</a>連結)。</p>


