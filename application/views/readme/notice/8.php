<h4>
    服務學習
</h4>
<div>
    (一)「學生服務學習施行辦法」經校務會議通過，本辦法重點如下：本校服務學習列為畢業審查條件，區分為「服務學習課程」與「非課程」之相關活動學習二部份。
</div>
<div>
    1.課程部份：必修/零學分。本校大一新生及轉學生，必修習之服務學習課程，共計二學期，零學分，每週2小時。
</div>
<div>
    2.非課程部份：學生學習護照；簡稱中大護照。
</div>
<ul>
    <li>
        非課程之相關活動學習部份，為本校大學部學生參與各項非具正式學分課程與活動之學習，並以學習護照呈現之。
    </li>
    <li>
        學生學習護照，區分為「基本學習」與「高階學習」二個層次。學生需完成基本學習100認證時數，始可畢業。
    </li>
    <li>
        護照三大學習領域，包含服務學習類、生活知能類與人文藝術類。
    </li>
    <li>
        「大一週會」為學生學習護照必選時數，一年級「需選四場次」（上學習選兩場＋下學期選兩場），另外還需參加「院週會兩次」（上學期一場＋下學期一場）。請務必於１０１年９月２１日前完成，報名請上「
        <a href="http://web.cc.ncu.edu.tw/~ncu7221/" title="生活輔導組資訊網">生活輔導組資訊網</a>」查詢。
    </li>
</ul>
<div>
    3.服務學習抵(減)免：
</div>
<ul>
    <li>
        轉學生倘於他校修習相關服務學習及格者，得申請抵(減)免。
    </li>
    <li>
        領有殘障手冊或特殊個案學生，經導師、系主任同意者，經學務處審查合格，得申請免修服務學習。
    </li>
</ul>
<div>
    (二)如有對服務學習有任何疑問，請洽服務學習辦公室黃裕隆先生/鄭雲鳳小姐/戴辰珊小姐諮詢與辦理(分機:57235～6)。
</div>