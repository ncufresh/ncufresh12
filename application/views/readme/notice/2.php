<h1>
學生學習講座</h1>
<p>
<strong>講座內容</strong><strong>：</strong>為協助各階段同學跨越學習瓶頸，並增廣各面向學習知識與經驗，教學發展中心每學期分群與校內教師或校外專業講師合作，辦理不同學習系列講題，主要包括有：新生入門、研究生學習與生涯知能學習講題。另外，更辦理成績表現優異學生學習經驗座談，不保留呈現大學四年學習生活、社團活動、個人情感協調之最佳密技！</p>
<p>
<strong>講座時間</strong>：系列活動將於開學公告於<strong>教學發展中心</strong>網站。</p>
<p>
<strong>報名方式</strong>：請上<a href="http://tldc.ncu.edu.tw"><strong>教學發展中心</strong></a>網站報名。</p>