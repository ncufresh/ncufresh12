<h4>
    學生學習護照實施流程
</h4>
<h5>
    第一階段 :申領護照
</h5>
<ol>
    <li>
        首本發放:本校新生於入學時,發放學生學習護照每人乙本。
    </li>
    <li>
        補(換)發:護照殘破、污損、遺失者,須至總務處出納組繳納工本材料費100元後,再向學務處申請補(換)發。
    </li>
    <li>
        補(換)發時，以學務處電腦記錄資料為準補之，學生對認證時數如有異議，需提出有效證明以供補登(列)。
    </li>
    <li>
        護照升級:通過基本學習認證者，可至學務處核印。
    </li>
</ol>
<h5>
    第二階段 :活動認證
</h5>
<ol>
    <li>
        活動單位可用上網、電話或書面方式，向學務處申請認證時數。
    </li>
    <li>
        承辦單位審查活動類型與內容,報請核定。
    </li>
    <li>
        核定後登錄資料，並回覆活動單位，送達認證紀錄貼紙給申請單位。
    </li>
    <li>
        未同意者，通知申請單位不核發認證時數。
    </li>
</ol>
<h5>
    第三階段 :活動參與
</h5>
<ol>
    <li>
        學生參與活動;依辦理單位之要求可採自由報名、事前報名、預約報名、指定參與等多種方式參與學習。
    </li>
    <li>
        承辦單位發給活動單位認證紀錄貼紙。
    </li>
    <li>
        活動單位對通過學習標準之學生，發放認證貼紙，供學生貼於護照。(學習通過標準由活動單位認定)
    </li>
</ol>
<h6>
    學生個人主動辦理認證
</h6>
<ol>
    <li>
        學生攜績效(能力)證明，至學務處申請審查認證，承辦單位本要點附件二標準，審予認證時數。
    </li>
    <li>
        校外服務學習認證，需填寫要點之附件五，向學務處提出認證，學務處依要點附件二標準，審予認證時數。
    </li>
    <li>
        經採認過之相關學習與績效(能力)證明，簽章註明並登記，避免再次提列。
    </li>
</ol>
<h5>
    第四階段:活動結束
</h5>
<ol>
    <li>
        活動單位將通過認證學生名冊(學號、姓名)一週內，以書面或上網方式，送至學務處登錄。
    </li>
    <li>
        學務處採信任之態度辦理認證作業，惟亦得抽檢活動單位辦理實況，或抽驗學生校外服務學習實況，如發現虛假依規定處份之。
    </li>
</ol>
<h5>
    第五階段 :獎勵
</h5>
<ol>
    <li>
        實施二次預警措施，以提醒、督促、輔導學生順利完成基本護照學習。
    </li>
    <li>
        畢業前(不含休學、復學、研修生)完成高階護照者，由學校頒給推薦書或獎狀，以為表彰與鼓勵。
    </li>
    <li>
        申請獎勵時程：學務處主動作業，畢業生亦得於四月底前，檢具護照向學務處提出。
    </li>
    <li>
        曾受記大過處份者，不予獎勵。
    </li>
    <li>
        大學部學生畢業時，未修滿基本學習認證時數，不得畢業。
    </li>
</ol>