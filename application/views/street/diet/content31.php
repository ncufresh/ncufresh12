<h4>
    後門
</h4>
<p>
    地點位在中大育成中心旁的門口，這一整條街販賣著各種不同的商品
</p>
<p>
    走在這一條街上，可以看到各式各樣的店家，有餐廳、體育用品店、便利商店、超市
</p>
<p>
    其熱鬧的程度，宛如是一個小型的中央商圈，另外在後門的早餐店更是中央學生的共同回憶
</p>
<p>
    中央大學的後門一直有著「早餐街」的美名，在這一條街上，有著超過１０家以上的早餐店
</p>
<p>
    其受歡迎程度從一位難求的情況可以看出來
</p>
<p>
    若假日不趕時間，不妨相約幾個夥伴來後門早餐店，享受中央大學特有的早餐文化
</p>