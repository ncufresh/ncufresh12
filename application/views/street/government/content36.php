<p>
    位於<strong>校門進來後左側、於據德樓旁</strong>，是一座磚紅色的端莊巍峨建築。
</p>
<p>
    大講堂可以容納將近500人，許多大師級的演講、各性質的表演、社團系所的成果發表以及各學院的例行週會都會在這裡進行。
</p>
<p>
    三樓則是展示學校歷史文物的地方，有學校沿革、校史紀要、歷屆校長、名人手稿、紀念文物、建築巡禮以及歷史照片等等，還有榮譽博士、傑出校友、相關書刊資料的展示。資料非常的詳實完備，是想認識中央大學歷史的好去處。
</p>
