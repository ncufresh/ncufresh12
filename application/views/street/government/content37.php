<h5>
    <strong>語言中心(Language Center)</strong>：
</h5>
<p>
    推行各項語言教學革新計畫，籌辦各類語文競賽與英檢能力測驗。
</p>
<p>
    詳情請至<strong><a href="http://www.lc.ncu.edu.tw/main/chinese/index.php" title="語言中心網站">語言中心網站</a></strong>查詢。
</p>
<h5>
    <strong>師資培育中心</strong>：
</h5>
<p>
    師培中心的業務為辦理中等學校師資培育工作，每年辦理教育學程的甄選，提供學生教師甄試相關資訊及就業輔導。
</p>
<p>
    詳情請至<strong><a href="http://tn.ncu.edu.tw/">師資培育中心網站</a></strong>查詢
</p>
<h5>
    <strong>微光電實驗室</strong>：
</h5>
<p>
    微光電實驗室之設計目的為提供高度潔淨的實驗空間，以提供4吋矽晶製程技術及砷化鎵高頻元件及光電元件製程技術之開發，來完成跨越多項學科之尖端研究計畫。
</p>
