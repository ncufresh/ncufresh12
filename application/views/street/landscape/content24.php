<h4>
    百花川
</h4>
<p>
    若想要認識中央大學秀麗的校園風景，「百花川」絕對是最棒的賞景步道之一
</p>
<p>
    沿著步道，渠道流水潺潺伴隨在旁，沿路上夾道的兩排林蔭幽靜高雅，兩側是校內寬廣的草皮區，視野十分舒暢。
</p>
