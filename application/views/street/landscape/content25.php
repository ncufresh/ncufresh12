<h4>
    國泰樹
</h4>
<div>
    地科系館前的大草坪，有兩株大榕樹，其中一株有一個響亮的名字叫國泰樹
</div>
<div>
    因為他樹蔭大且樹形半圓，如同早期國泰人壽廣告的大樹，所以學生都暱稱他為國泰樹，是地科院大草坪的特色招牌
</div>
<div>
    因為碧綠的芳草與雄偉的榕樹，搭配有藍天的美景，國泰樹成為中央大學的拍照勝地之一
</div>
<div>
    在這裡，常常可以看到婚紗攝影的進行，為國泰樹增添了幾絲浪漫
</div>
<div>
    平日清晨，可以看到很多人在這裡做運動
</div>
<div>
    假日時分，攜家帶眷的身影絡繹不絕，常常可以看到許多大人與小孩在這裡野餐、遊玩
</div>
<div>
    許多偶像劇也都曾到國泰樹進行取景
</div>
