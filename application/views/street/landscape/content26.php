<h4>
    筆墨紙硯
</h4>
<p>
    <u>筆</u>：大門口聳立的在台復校紀念碑，尖頭端正如同一支筆。
</p>
<p>
    <u>墨</u>：校門入口處右方的中大派出所，形狀方正，灰黑色的屋頂就像墨條。
</p>
<p>
    <u>紙</u>：走進大門後，正前方映入眼簾的是行政大樓，外牆方格狀設計如稿紙一般。
</p>
<p>
    <u>硯</u>：校門前氣派的圓環中央，有著校徽和校名的綠色花圃，呈現橢圓狀好像硯台一樣。
</p>
