<h4>
    綠草如茵
</h4>
<p>
    廣大且漂亮草坪是中央的驕傲!
</p>
<p>
    中央有不少校地是草地，兼具了環保、休閒、活動等功能，以下是學校幾個重要的草坪：
</p>
<h5>
    1.<u>壘球場位處的大草坪</u>：
</h5>
<p>
    這個大草坪是學校為棒壘球的活動特別留的綠地，屬「永久的綠地」。
</p>
<p>
    期望中央學學子要課餘要出外多活動，因此時常可以看到校隊、系隊在壘球場進行練習。
</p>
<p>
    <img alt="綠草如茵" src="<?php echo Yii::app()->request->baseUrl; ?>/statics/documents/1%20(3).jpg" style="width:340px;height:240px;" />
</p>








<h5>
    2.<u>中正圖書館前的大草坪</u>：
</h5>
<p>
    在這個大草坪上有兩株大榕樹，其中一株有一個響亮的名字叫：國泰樹，是這個草坪的特色招牌。
</p>
<p>
    國泰樹除了是同學和家人野餐、遊玩的好地點，也是婚紗攝影最喜歡的取景地，在這裡常常可以看到婚紗照的拍攝。
</p>
<p>
    更重要的是，許多偶像劇取景也會到國泰樹進行取景。
</p>
<p>
    <img alt="綠草如茵" src="<?php echo Yii::app()->request->baseUrl; ?>/statics/documents/2%20(1).jpg" style="width:320px;height:240px;" />
</p>
<h5>
    3.<u>文學院旁的太極草坪</u>：
</h5>
<p>
    在這個草坪有朱銘先生著名的銅雕作品「太極」，是中央大學重要的人文藝術景觀，也是遊客來到中央大學必會留下合影的地點。
</p>
