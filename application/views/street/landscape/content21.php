<h4>
    漫步雲端
</h4>
<p>
    這些由漢白玉石雕刻成的雲朵，散佈於國際學生宿舍和男四舍前草地，由林建榮設計製作。
</p>
<p>
    此12件尺寸迥異各不相同的雲形雕刻，除了觀賞，亦可作為行人坐椅使用，讓人在忙碌中喘一口氣。
</p>
<p>
    夜晚時分，透過內搭的LED照明設備，還可欣賞燈影明滅、雲朵漂浮的視覺之美。
</p>
