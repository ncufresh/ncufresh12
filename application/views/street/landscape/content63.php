<h4>
    女14舍前廣場
</h4>
<p>
    女十四舍前的小廣場，連接了正門與宵夜街，因此可常常看見人群來來往往的情形。
</p>
<p>
    由廣場往宵夜街方向會經過一餐，校內的社團常常會在一餐內或一餐前面的空地演出，吸引人群駐留。
</p>
<p>
    除此之外，一餐外也會懸掛著社團的巨海創作，或者是活動的立體宣傳創作，可說是學生的創意天地。
</p>
<p>
    廣場周圍，有許多可供人休息的座椅，天然的樹蔭是清幽的遮陽棚，到夜晚則是朦朧的燈景亮起。
</p>
<p>
    所以，不管是白天或黑夜，常可看到情侶、好友、在這廣場上聊天，閒情逸致，舒緩生活上的憂煩。
</p>
