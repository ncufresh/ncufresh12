<h5>
	公共藝術-坐雲飛想
</h5>
<p>
	烏龜池畔，目前正在施工的「中大會館」前，也有一漂亮的公共藝術景觀，待中大會館完工後將可一窺究竟：
</p>
<p>
    《坐雲飛想》融合東方文化的意象，透過停駐於雲端上的松果，指涉時間的流動與生命的起落。中國傳統的捲曲雲朵圖案，與形象類似香爐的種子，共同以具像形體，重新鑄造生命旅程的記憶。張乃文的《坐雲飛想》以白雲浮雕，結合金屬、大理石的立體雕塑，為中大會館與校園空間增添了別具禪意與超現實的趣味想像。
</p>
