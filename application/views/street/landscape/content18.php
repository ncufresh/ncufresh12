    幸運的話，在烏龜池邊可見到烏龜探出水面裡。
</p>
<p>
    而烏龜池對中大生來說有特別的意義。
</p>
<p>
    每年每逢同學的生日，總有不少的幸運兒被好友們丟進烏龜池
</p>
<p>
    這項傳統歷屆的傳承，巖然成了各位同學美好的回憶。
</p>
