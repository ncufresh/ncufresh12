<h5>
	公共藝術-DNA交響曲
</h5>
<p>
	為了展示生命科學學科的研究動向，即科學五館師生所投入的活動成果，楊奉琛以DNA纏繞而上的雙螺旋體為作品的發想概念。《DNA交響曲》以不鏽鋼材料創作虛實交錯的螺旋體造型，營造出富有律動性與豐富變化的視覺效果，其材質表面會隨著光線產生變化。
</p>
