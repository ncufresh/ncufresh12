<h5>
	公共藝術-為什麼
</h5>
<p>
	<strong><u>有新的發現都是從『為什麼』開始</u></strong>
</p>
<p>
	此三件一組的《為什麼》包含三個造型各異之立體裝置，分別是以鏡面材質反射四周事物的＜水滴＞；可供休憩，表面光滑如水面的＜長凳＞；以及能接收環境聲音的圓弧狀＜屏風＞。黎志文藉簡練的抽象造型，傳達人類以收集、觀察、研究、實驗來了解和改善世界運行的現象及理想，在累積的過程中不斷好奇探問，進而改善整體人類的生存條件。在藝術家的巧思設計下，《為什麼》雖然是靜態的作品，但卻能觀者產生互動，可說審美、娛樂、與實用價值兼備。
</p>
