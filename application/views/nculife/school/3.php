<h1>
校園劇場</h1>
<h2>
<a href="http://theatre.ncu.edu.tw/"><strong>黑盒子</strong></a></h2>
<p>
位於中壢市「國立中央大學」人社大樓中，在設計初始便希望打造一座專業的實驗劇場，引進專業經營團隊，為中大師生量身打造優質表演藝術活動，開拓學生美學視野，為理工學生培養深厚人文蘊涵，為台灣未來科技產業甚至表演藝術事業提供另類轉型模式。</p>
