<h1>
野味</h1>
<p>
此屬口味實在的便當店，份量相當大，肉類食品也非常好吃，且價格不會太高，中大學生享有五元折扣，有附送冬瓜茶，內用也有熱湯可享用，想要吃好又吃飽來這就對了！</p>
