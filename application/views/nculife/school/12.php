<h1>
香草庭園2F</h1>
<p>
一道綠意盎然的階梯，上樓之後是格局寬敞的香草庭園餐廳，賣有義大利麵、焗烤等經濟簡餐，落地窗外可見校園美景，適合聚餐、相當有氣氛的一家餐廳。</p>
