<h1>
郵件快遞</h1>
<p>
本校的郵件招領是由<strong>文書組</strong>負責。</p>
<p>
若有掛號郵件或包裹等要領取，可依以下步驟：</p>
<ol>
<li>
進入<strong>portal</strong><strong>入口</strong>&rarr;「<strong>郵務管理」</strong>查詢，並列印「提貨單」。</li>
<li>
接著再將「提貨單」及「學生證」（或有照片的證件）帶至行政大樓一樓右手邊的<strong>文書組收發室</strong>領去郵件包裹。（若代領，需攜帶<strong>本人及代領人學生證</strong>）</li>
<li>
也可以到二進位的NCU_News板上查詢掛號的郵件包裹。</li>
</ol>
<p>
?領取Time：<strong>8:00-17:00(中午12:00-13:30休息)</strong></p>
<p>
　若需領取當日郵件，必須等到當天13:30郵件工人處理完之後才可領取。</p>
<p>
?寄信地址：<strong>學校地址+宿舍+房號</strong></p>
<p>
　學校地址─「32001 桃園縣中壢市五權里2鄰中大路300號」</p>

<p>
※<strong>信件只送達文書組，不會寄到宿舍。</strong></p>
<ol>
<li>
<strong>掛號信：</strong>學校郵件招領系統有「以學校計中sparc帳號通知掛號郵件」功能。寄信時，可請寄信人在<strong>掛號信上附收件人的學號</strong>，系統將會自動通知郵件已到達。</li>
<li>
<strong>平信：</strong>不包含在郵件招領系統服務範圍內。平信會由宿舍幹部到文書組領取，再分送到宿舍公共區域的寢室信箱。</li>
<li>
<strong>宅急便包裹</strong>：直接送到宿舍門口，要請寄收包裹的同學注意一下！</li>
</ol>
