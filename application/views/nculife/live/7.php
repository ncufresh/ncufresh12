<strong>寢室設備</strong>： 4人房，有床組、衣櫃、書桌、冷氣機（需至校內便利商店購買冷氣儲值卡），屬公共衛浴。床組：多為上舖，地下是為下鋪，<strong>187cm&times;88cm</strong>。</p>
