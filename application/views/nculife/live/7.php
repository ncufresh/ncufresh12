<li>
<strong>交誼廳</strong>：採簡約風格傢俱，有電視機及沙發，提供當天的報紙與當期雜誌，學生可在此用餐、討論、看電視。</li>
<li>
<strong>桌球室</strong>：提供桌球桌，可讓學生在課堂間暇之餘活動筋骨</li>
<li>
<strong>自修室</strong>：有廣大的閱讀空間及桌椅，可於此K書或進行功課研討</li>
<li>
<strong>簡易廚房</strong>：提供冰箱、微波爐、電鍋等簡易烹飪廚具，學生可以於此烹飪個人餐點。</li>
<li>
<strong>公共設備</strong>：洗衣機（10元/次）、烘衣機（10元/次）、脫水機（免費）、飲水機。<strong></strong></li>
</ol>
