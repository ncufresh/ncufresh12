<p style="margin-left: 35.4pt; ">
	<strong>寢室設備</strong>：4人房，有床組、衣櫃、書桌、冷氣機(需至校內便利商店購買冷氣儲值卡) ，屬公共衛浴。</p>
<p>
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 床組：皆為上舖，<strong>186cm&times;100cm</strong>。 &nbsp;&nbsp;</p>
<p>
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;<img alt="" src="/upload/images/images/G1_1-2.jpg" style="text-align: center; width: 240px; height: 180px; " /><span style="text-align: center; ">&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span><img alt="" src="/upload/images/images/%E5%AF%A2%E5%AE%A4.jpg" style="text-align: center; width: 135px; height: 180px; " /></p>
<p style="margin-left:35.4pt;">
	<strong>門禁</strong>：夜間12點熄寢室大燈（仍可開桌燈、走廊燈及衛浴空間的燈）</p>
<p style="margin-left:35.4pt;">
	&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp; 外圍大門及宿舍門口會有門禁，但使用門禁卡(即學生證)，就可進入。</p>
<p style="margin-left:35.4pt;">
	&nbsp;<img alt="" src="/upload/images/images/IMG_2953.JPG" style="width: 240px; height: 180px; " />&nbsp; &nbsp; &nbsp;<img alt="" src="/upload/images/images/IMG_2954.JPG" style="width: 320px; height: 180px; " /></p>
