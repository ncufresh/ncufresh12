<p style="margin-left:35.4pt;">
	1.<strong>交誼廳</strong>：採簡約風格傢俱，有電視機及沙發，提供當天的報紙與當期雜誌，學生可在此用餐、討論、看電視。</p>
<p style="margin-left:35.4pt;">
	2.<strong>簡易廚房</strong>：提供冰箱、微波爐、電鍋等簡易烹飪廚具，學生可以於此烹飪個人餐點。</p>
<p style="margin-left:35.4pt;">
	3.<strong>閱覽室</strong>：位於女1.3舍之間的2、3樓，提供提供寧靜閱讀空間，期中末考時是同學最佳閱讀場所。</p>
<p style="margin-left:35.4pt;">
	4.<strong>公共設備</strong>：洗衣機(10元/次)、烘衣機(10元/次)、脫水機(免費)、飲水機。</p>
<p style="margin-left:35.4pt;">
	5.<strong>垃圾車</strong>：每週一至週六的<strong>18：58</strong>左右，在女一到四舍大門口。</p>
