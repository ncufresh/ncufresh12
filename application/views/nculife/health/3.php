2.初談</h5>
<p>
    <strong>為什麼要初談？</strong>
</p>
<p>
    目的在協助你釐清自己的需求，由初談老師與你進行短暫會談，為你安排合適之心理師，也解答你對諮商的需求或期待，以利後續諮商之進行。
</p>
<ul>
    <li>
        時間：約10~15分鐘
    </li>
</ul>
<p>
    （1）了解你想談的議題與諮商需求。
</p>
<p>
    （2）解說諮商規則。
</p>
<p>
    （3）約定正式諮商時間。
</p>
