<h5>
    中原夜市-由申甲
</h5>
<p>
    位於中原夜市裡的由申甲文具店，總是充滿著滿滿的人潮，是全中壢地區最大的文具、美工用品店，規模有地下一層地上六層，由申甲內各類文具一應俱全，可以一次買齊所需用品。
</p>
<p>
    營業時間：09:30~21:30
</p>
