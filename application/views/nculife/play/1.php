<h2>
<a href="http://www.u2mtv.com/">U2電影館</a></h2>
<p>
U2是知名連鎖二輪包廂式電影院，於2011年暑假進駐中壢，寬敞的大廳、親切專業的服務態度，並設有高級放映設備和豪華舒適的包廂，是中央學生不可不知的休閒娛樂場所。</p>
