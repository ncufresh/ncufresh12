<h2>
<a href="http://www.venice-cinemas.com.tw/">威尼斯影城</a></h2>
<p>
是中壢市最大的電影院，影城坐落於二棟歐式建築裡，兩棟建築藉由仿威尼斯水都的情人空橋相互連結，如同它的名子「威尼斯」。威尼斯擁有最先進的影音設備，豪華劇院看台式的座位，廣角寬幅的螢幕，提供觀眾最高級且專業的享受。</p>
