<h4>
    腳踏車
</h4>
<p>
    1.校內主要使用腳踏車為代步工具。請注意，學校常有汽機車及環校公車行駛，同學騎乘腳踏車時請特別注意行車安全。停放腳踏車的位置為腳踏車架或規定的區域內，若違反規定，腳踏車將被鎖車一個星期。當腳踏車需要修理時，後門及宵夜街各有一家腳踏車行可以進行維修。如果輪胎需要充氣，在正門及後門警衛室旁皆設有打氣裝置，提供同學免費使用。
</p>
